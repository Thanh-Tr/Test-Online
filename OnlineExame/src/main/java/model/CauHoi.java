package model;

public class CauHoi {
	private String maCH;
	private String moTaCH;
	private String cauA;
	private String cauB;
	private String cauC;
	private String cauD;
	private String dapAn;
	private String maDT;
	public CauHoi() {}
	public CauHoi(String maCH, String moTaCH, String cauA, String cauB, String cauC, String cauD, String dapAn,
			String maDT) {
		this.maCH = maCH;
		this.moTaCH = moTaCH;
		this.cauA = cauA;
		this.cauB = cauB;
		this.cauC = cauC;
		this.cauD = cauD;
		this.dapAn = dapAn;
		this.maDT = maDT;
	}
	public String getMaCH() {
		return maCH;
	}
	public void setMaCH(String maCH) {
		this.maCH = maCH;
	}
	public String getMoTaCH() {
		return moTaCH;
	}
	public void setMoTaCH(String moTaCH) {
		this.moTaCH = moTaCH;
	}
	public String getCauA() {
		return cauA;
	}
	public void setCauA(String cauA) {
		this.cauA = cauA;
	}
	public String getCauB() {
		return cauB;
	}
	public void setCauB(String cauB) {
		this.cauB = cauB;
	}
	public String getCauC() {
		return cauC;
	}
	public void setCauC(String cauC) {
		this.cauC = cauC;
	}
	public String getCauD() {
		return cauD;
	}
	public void setCauD(String cauD) {
		this.cauD = cauD;
	}
	public String getDapAn() {
		return dapAn;
	}
	public void setDapAn(String dapAn) {
		this.dapAn = dapAn;
	}
	public String getMaDT() {
		return maDT;
	}
	public void setMaDT(String maDT) {
		this.maDT = maDT;
	}
	
	
}
